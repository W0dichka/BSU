import pandas as pd

# Создание датафрейма с примерами данных об уровне образования
data = {
    'Уровень образования': ['Среднее', 'Высшее', 'Неоконченное высшее', 'Послевузовское', 'Среднее специальное'],
    'Температура': [25, 20, 15, 10, 30],  # Например, температурные данные
    'Рейтинг': [2, 5, 3, 1, 4]  # Порядковая шкала
}

df = pd.DataFrame(data)

# Функция для преобразования данных
def transform_data(df):
    # Преобразование в номинальную шкалу (категориальные данные)
    df['Уровень образования'] = pd.Categorical(df['Уровень образования'])
    
    # Преобразование в порядковую шкалу (например, рейтинг)
    df['Рейтинг'] = pd.Categorical(df['Рейтинг'], ordered=True)
    
    return df

# Применение функции
transformed_df = transform_data(df)

# Вывод преобразованного датафрейма
print(transformed_df)
print(transformed_df.dtypes)